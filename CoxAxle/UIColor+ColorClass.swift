import Foundation
import UIKit

extension UIColor {
    
    class func WhiteColor() -> UIColor {
        return UIColor.init(red: 255/255.0, green: 255/255.0, blue: 255/255.0, alpha: 1)
    }
    
    class func CharcoalGrey() -> UIColor {
        return UIColor.init(red: 64/255.0, green: 68/255.0, blue: 71/255.0, alpha: 1)
    }
    
    class func BlackColor() -> UIColor {
        return UIColor.init(red: 0/255.0, green: 0/255.0, blue: 0/255.0, alpha: 1)
    }
    
    class func AzureColor() -> UIColor {
        return UIColor.init(red: 0/255.0, green: 164/255.0, blue: 237/255.0, alpha: 1)
    }
    
    class func PumpkinColor() -> UIColor {
        return UIColor.init(red: 238/255.0, green: 119/255.0, blue: 0/255.0, alpha: 1)
    }
    
    class func LightWhite() -> UIColor {
        return UIColor.init(red: 232/255.0, green: 232/255.0, blue: 232/255.0, alpha: 1)
    }
    
    class func Slate20Color() -> UIColor {
        return UIColor.init(red: 78/255.0, green: 89/255.0, blue: 111/255.0, alpha: 0.20)
    }
    
    class func SlateColor() -> UIColor {
        return UIColor.init(red: 78/255.0, green: 90/255.0, blue: 111/255.0, alpha: 1)
    }
    
    class func PinkishGrey() -> UIColor {
        return UIColor.init(red: 204/255.0, green: 204/255.0, blue: 204/255.0, alpha: 1)
    }
    
    class func FadedOrange() -> UIColor {
        return UIColor.init(red: 237/255.0, green: 113/255.0, blue: 97/255.0, alpha: 1)
    }
    
    class func lightgrayColor() -> UIColor {
        return UIColor.init(red: 178/255.0, green: 179/255.0, blue: 180/255.0, alpha: 1)
    }
    
}